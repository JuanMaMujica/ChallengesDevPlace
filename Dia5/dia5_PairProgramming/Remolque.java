package dia5_PairProgramming;

public class Remolque {
	private double peso;
	private String matricula;

	public Remolque(double peso, String matricula) {
		this.peso = peso;
		this.matricula = matricula;
	}

	public double getPeso() {
		return peso;
	}

	public String getMatricula() {
		return matricula;
	}

	
}
